/**
 * استخراج الكيانات مع suggest (related articles)
 */
async function extractEntitiesComplete(text) {
  const startTime = Date.now();
  
  const entities = {
    articles: [],
    numbers: [],
    systems: [],
    courts: [],
    suggested: [] // المواد المقترحة (من related)
  };
  
  const textLower = text.toLowerCase();
  const matchedMappings = [];
  
  for (const mapping of COMPLETE_MAPPING) {
    for (const keyword of mapping.keywords) {
      if (textLower.includes(keyword.toLowerCase())) {
        entities.articles.push(...mapping.articles);
        entities.numbers.push(...mapping.articles);
        entities.systems.push(...mapping.systems);
        matchedMappings.push(mapping);
        
        // إضافة suggested articles
        if (mapping.suggest) {
          entities.suggested.push(...mapping.suggest);
        }
        
        // إضافة related من المواد المطابقة
        if (mapping.related) {
          for (const article of mapping.articles) {
            if (mapping.related[article]) {
              entities.suggested.push(...mapping.related[article]);
            }
          }
        }
        
        break;
      }
    }
  }
  
  // إزالة التكرار
  entities.articles = [...new Set(entities.articles)];
  entities.numbers = [...new Set(entities.numbers)];
  entities.systems = [...new Set(entities.systems)];
  entities.suggested = [...new Set(entities.suggested)]
    .filter(s => !entities.articles.includes(s)); // لا تكرار مع الموجود
  
  return {
    entities,
    latency: Date.now() - startTime,
    mappingsMatched: matchedMappings.length
  };
}

